package Model;

public class DT48 {
    private int id;
    private String tenDT;

    public DT48() {
    }

    public DT48(int id, String tenDT) {
        this.id = id;
        this.tenDT = tenDT;
    }

    public int getId() {
        return id;
    }

    public String getTenDT() {
        return tenDT;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setTenDT(String tenDT) {
        this.tenDT = tenDT;
    }
}
