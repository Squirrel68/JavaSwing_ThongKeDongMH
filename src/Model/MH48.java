package Model;

public class MH48 {
    private int id;
    private String matHang;

    public MH48() {
    }

    public MH48(int id, String matHang) {
        this.id = id;
        this.matHang = matHang;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMatHang() {
        return matHang;
    }

    public void setMatHang(String matHang) {
        this.matHang = matHang;
    }
}
