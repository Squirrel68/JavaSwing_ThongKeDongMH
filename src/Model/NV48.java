package Model;

public class NV48 {
    private int id;
    private String tenNV;

    public NV48() {
    }

    public NV48(int id, String tenNV) {
        this.id = id;
        this.tenNV = tenNV;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenNV() {
        return tenNV;
    }

    public void setTenNV(String tenNV) {
        this.tenNV = tenNV;
    }
}
